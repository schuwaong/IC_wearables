const CURATED_AFFILIATE_CATALOG = {
  HK: HK_PRODUCTS,
  MY: MY_PRODUCTS,
};

function uniqueKeys(keys = []) {
  return keys.filter((key, index, all) => key && all.indexOf(key) === index);
}

function queryCandidates(searchQuery = "") {
  const normalized = String(searchQuery || "").toLowerCase();
  const candidates = [];

  if (/\b(technical|performance|running)\b/.test(normalized) && /\bjacket\b/.test(normalized)) {
    candidates.push("technical_jacket", "jacket", "cropped_jacket");
  }
  if (/\bcropped\b/.test(normalized) && /\bjacket\b/.test(normalized)) {
    candidates.push("cropped_jacket", "jacket", "blazer");
  }
  if (/\b(wool|cashmere)\b/.test(normalized) && /\bcoat\b/.test(normalized)) {
    candidates.push("wool_coat", "cardigan", "jacket");
  }
  if (/\bleggings?\b/.test(normalized)) candidates.push("leggings", "jogger");
  if (/\bjoggers?\b/.test(normalized)) candidates.push("jogger", "leggings");

  const hasSneakerSignal =
    /\b(sneaker|sneakers|running shoe|running shoes|training sneaker|training sneakers)\b/.test(normalized) ||
    (/\bshoe|shoes\b/.test(normalized) && /\b(clean|minimal|casual|training|running)\b/.test(normalized));
  if (hasSneakerSignal) {
    if (/\b(clean|minimal|casual|city)\b/.test(normalized)) candidates.push("casual_sneaker", "sneaker", "flat");
    else candidates.push("sneaker", "casual_sneaker", "flat");
  }

  if (/\b(tote|bag)\b/.test(normalized)) candidates.push("tote", "flat", "loafer", "casual_sneaker");
  if (/\b(loafer|loafers|slingback|pump|pumps|heel|heels)\b/.test(normalized)) {
    candidates.push("loafer", "flat", "casual_sneaker", "sneaker");
  }
  if (/\bblazer\b/.test(normalized)) candidates.push("blazer", "cropped_jacket");
  if (/\b(blouse|shirt|silk|satin|near-face)\b/.test(normalized)) candidates.push("blouse", "knit");
  if (/\b(tank|tee|t-shirt|t shirt)\b/.test(normalized)) candidates.push("tee", "knit", "blouse");
  if (/\b(cardigan|cashmere)\b/.test(normalized)) candidates.push("cardigan", "knit", "wool_coat");
  if (/\b(knit|sweater|jumper|top)\b/.test(normalized)) candidates.push("knit", "cardigan", "tee", "blouse");
  if (/\b(trouser|trousers|pants|skirt|denim|jeans|palazzo)\b/.test(normalized)) {
    candidates.push("trouser", "jogger", "leggings");
  }
  if (/\bjacket\b/.test(normalized)) candidates.push("jacket", "technical_jacket", "cropped_jacket", "blazer");

  return uniqueKeys(candidates);
}

export function hasCuratedAffiliateCatalog(countryCode = "") {
  return Boolean(CURATED_AFFILIATE_CATALOG[String(countryCode || "").trim().toUpperCase()]);
}

export function findCuratedAffiliateProducts(countryCode = "", searchQuery = "") {
  const marketCatalog = CURATED_AFFILIATE_CATALOG[String(countryCode || "").trim().toUpperCase()];
  if (!marketCatalog) return [];

  for (const key of queryCandidates(searchQuery)) {
    const matches = marketCatalog[key];
    if (Array.isArray(matches) && matches.length) return matches.map((product) => ({ ...product }));
  }

  return [];
}
